def batch_extract_info_neo4j(num: str, source_value: str, target: str, prompt: str):
    """
    从 neo4j 获取 Chunk 数据，提取信息后写回职业类别节点属性。

    :param num: 职业类别节点 ID
    :param source_type: 来源类型，"knowledge"（知识类型）或 "property"（属性类型）
    :param source_value: 具体值
                         - knowledge: 知识类型（综合素质、职业技能、证书、工作内容、专业、工作经验）
                         - property: 属性类型（学历要求、晋升路径）
    :param target: 目标属性名，将作为职业类别节点的新属性
    :param prompt: 提示词
    :return: 处理后的信息
    """
    fp = FileProcessor(f"need_data/{num}_{source_value}.json")
    dic = fp.read()

    jt = dic_map[num]

    # Step 2: 构建 prompt 并调用 LLM
    p = f'''
    你是一个专业的岗位画像分析助手。你的任务是从多个岗位的原始描述中，提取并生成【{target}】的准确描述和标签。
    
    ### 核心原则（必须严格遵守）：
    1. **真实性第一**：所有提取的内容必须有明确的原文依据，严禁无中生有、臆想编造
    2. **证据链完整**：每个特征/标签都必须能追溯到至少一条原文描述
    3. **过滤噪声**：自动识别并忽略无效内容（如："这是一个招聘信息"、空字符串、纯标点等）
    4. **宁缺毋滥**：如果原文信息不足以支撑某个特征，宁可放弃也不要强行提取
    
    ### 任务要求：
    {prompt}
    
    ### 输入数据说明：
    你将收到一个 JSON 对象，其中：
    - **键（Key）**：初步提取的特征/标签名称
    - **值（Value）**：该特征对应的多条原始岗位描述文本数组
    
    ### 处理步骤：
    
    #### 第一步：数据清洗与验证
    对每个特征进行严格验证：
    1. **过滤无效原文**：剔除以下类型的原文：
       - 包含"这是一个招聘信息"的文本
       - 纯空白、纯标点符号的文本
       - 长度小于5个字符的文本
       - 与特征明显无关的文本（如特征是"Python编程"，原文是"团队合作精神"）
    
    2. **特征有效性判断**：
       - 检查特征是否有至少1条有效原文支撑
       - 如果没有有效原文，直接丢弃该特征
       - 标记每个特征的置信度（高/中/低）
    
    #### 第二步：特征整合与去重
    1. **合并相似特征**：将语义相近的特征合并（如"React开发"和"React框架使用"）
    2. **消除矛盾**：如果同一特征的不同原文存在矛盾，标注冲突点
    3. **频次统计**：记录每个特征在多少条原文中出现
    
    #### 第三步：生成岗位画像描述
    基于验证后的特征，生成一段连贯的岗位画像描述：
    - 突出高频、高置信度的核心要求
    - 区分"必备技能"和"加分项"
    - 保持语言专业、简洁、准确
    - 控制在200-500字之间
    
    #### 第四步：提取标准化标签
    从有效特征中提取5-15个关键标签：
    - 标签应该是名词短语或动宾结构（如："React开发"、"团队协作"）
    - 避免过于宽泛的标签（如："技术能力"）
    - 避免过于具体的实现细节（如："使用Axios发起GET请求"）
    - 按重要性排序
    
    ### 输出格式要求：
    仅返回一个标准的 JSON 对象，格式如下：
    {{
      "{target}_description": "岗位画像的具体描述文本（200-500字）",
      "{target}_tags": ["标签1", "标签2", "标签3", ...],
      "{target}_features": [
        {{
          "feature_name": "特征名称",
          "confidence": "high/medium/low",
          "frequency": 出现次数,
          "evidence_count": 有效原文数量,
          "sample_evidence": ["原文1", "原文2"]
        }}
      ],
      "_metadata": {{
        "total_raw_items": 原始特征总数,
        "valid_features": 有效特征数,
        "filtered_noise_count": 过滤的噪声数量,
        "processing_notes": "处理说明（如有特殊情况）"
      }}
    }}
    
    ### 质量检查清单（生成前自检）：
    ✓ 每个标签都能在 {target}_features 中找到对应特征
    ✓ 描述中没有出现原文中不存在的信息
    ✓ 已过滤所有"这是一个招聘信息"等噪声数据
    ✓ 特征与原文的相关性经过人工可验证
    ✓ JSON 格式正确，可被 json.loads() 直接解析
    
    ### 提供的信息：
    {json.dumps(dic, ensure_ascii=False, indent=2)}
    
    现在请开始分析，严格按照上述要求输出 JSON 结果：
    '''

    raw_response = model.call_ollama(p)

    if not raw_response:
        print(f"职业类别 (key={jt}) 信息解析失败")
        return

    # Step 3: 将结果写入职业类别节点
    result_value = raw_response.get(f"{target}_description")
    if result_value is not None:
        if jt not in dic_result:
            dic_result[jt] = {}
        dic_result[jt][f"{target}_description"] = result_value
    else:
        print(f"LLM 返回结果中未找到 {target} 字段")

    fp_result.save(dic_result)
    fp_log = FileProcessor(f"log/{num}_{target}.json")
    fp_log.save(raw_response)
    print(f"{jt} - {target} 已完成")


def batch_score_neo4j(num: str, source_value: str, target: str, prompt: str):
    """
    通用的素质评分函数，根据提供的原始数据和评价标准进行量化打分。

    :param num: 职业类别节点 ID
    :param source_value: 数据来源（如 "综合素质"）
    :param target: 目标评分维度名称（如 "创新能力评分"）
    :param prompt: 评价标准和任务要求，需包含：
                   - 该维度的定义和内涵
                   - 评分标准（0-100分的具体划分）
                   - 关键词参考（可选）
                   - 其他特殊要求
    :return: 处理后的评分结果
    """
    fp = FileProcessor(f"need_data/{num}_{source_value}.json")
    dic = fp.read()

    jt = dic_map[num]

    # 构建通用评分 prompt
    p = f'''
    你是一个专业的岗位素质评估专家。请根据提供的原始数据，按照给定的评价标准对【{target}】进行量化评分。
    
    ### 评价标准和要求：
    {prompt}
    
    ### 数据处理流程：
    
    #### 第一步：数据清洗
    严格过滤无效原文：
    - 剔除包含"这是一个招聘信息"的文本
    - 剔除纯空白、纯标点符号的文本
    - 剔除长度小于5个字符的文本
    - 剔除与评估维度明显无关的内容
    
    记录：
    - 原始数据条数
    - 有效证据数量
    - 过滤的噪声数量
    
    #### 第二步：证据提取与分析
    1. 在原文中搜索与【{target}】相关的描述
    2. 统计相关证据的数量和分布
    3. 提取最具代表性的3-5条原文作为评分依据
    4. 分析原文中表达的重视程度：
       - 强制性要求（必须、需要、要求）
       - 优先性要求（优先、加分、青睐）
       - 期望性要求（希望、最好、理想）
    
    #### 第三步：综合评分（满分100分）
    基于以下维度计算最终得分：
    
    **证据充分性 (35%)**
    - 相关证据的数量和质量
    - 证据的多样性和覆盖面
    
    **强调程度 (30%)**
    - 强制性要求的占比
    - 原文中表达的紧迫性和重要性
    
    **覆盖广度 (20%)**
    - 不同特征/场景中涉及该能力的比例
    - 在整体需求中的普遍性
    
    **一致性 (15%)**
    - 各证据之间的连贯性
    - 是否存在矛盾或冲突
    
    计算公式：
    最终得分 = 证据充分性×0.35 + 强调程度×0.30 + 覆盖广度×0.20 + 一致性×0.15
    
    #### 第四步：生成分级标签
    根据分数确定等级：
    - **优秀 (90-100分)**：极高要求，核心竞争力的关键因素
    - **良好 (75-89分)**：高要求，重要的能力指标
    - **中等 (60-74分)**：中等要求，较为重要但非核心
    - **一般 (40-59分)**：基本要求，有一定关注
    - **较低 (0-39分)**：几乎无要求或极少提及
    
    #### 第五步：生成评分理由
    提供清晰、具体的评分依据：
    - 总结关键发现（50-100字）
    - 引用2-3条最具代表性的原文
    - 说明该能力在岗位中的重要程度
    
    ### 输出格式要求：
    仅返回一个标准的 JSON 对象，格式如下：
    {{
      "{target}": {{
        "score": 85,
        "level": "良好",
        "reason": "该岗位对{target}有较高要求，在多条原文中明确提到...（具体、客观的评分依据）",
        "evidence": [
          "最能体现{target}的原文描述1",
          "次重要的原文描述2",
          "补充说明的原文描述3"
        ],
        "scoring_breakdown": {{
          "evidence_adequacy": 88,
          "emphasis_level": 82,
          "coverage_breadth": 85,
          "consistency": 86
        }},
        "statistics": {{
          "keyword_matches": 15,
          "evidence_count": 8,
          "importance_distribution": {{
            "mandatory": 3,
            "preferred": 5,
            "desired": 2
          }}
        }}
      }},
      "_metadata": {{
        "job_type": "{jt}",
        "evaluation_dimension": "{target}",
        "data_source": "{num}_{source_value}.json",
        "total_raw_items": {len(dic)},
        "valid_evidence_count": 实际有效证据数,
        "filtered_noise_count": 过滤的噪声数量,
        "scoring_method": "multi_dimensional_weighted_scoring",
        "max_score": 100,
        "processing_notes": "如有特殊情况在此说明，否则为空字符串"
      }}
    }}
    
    ### 质量检查清单（生成前必须自检）：
    ✓ score 是 0-100 的整数或保留一位小数
    ✓ level 必须是以下之一："优秀"、"良好"、"中等"、"一般"、"较低"
    ✓ reason 具体、客观，基于实际观察到的证据，不是空泛描述
    ✓ evidence 中的每条原文都与【{target}】直接相关
    ✓ scoring_breakdown 中四个子项都是 0-100 的数值
    ✓ statistics 中的数据与实际分析一致
    ✓ importance_distribution 的三个值之和等于 evidence_count
    ✓ _metadata 中的所有字段都已正确填充
    ✓ JSON 格式完全正确，可被 json.loads() 直接解析
    ✓ 没有编造任何原文中不存在的信息
    
    ### 提供的原始数据：
    {json.dumps(dic, ensure_ascii=False, indent=2)}
    
    现在请严格按照上述评价标准和输出格式，对【{target}】进行评分：
    '''

    raw_response = model.call_ollama(p)

    if not raw_response:
        print(f"职业类别 (key={jt}) - {target} 评分失败")
        return

    # 将结果写入结果字典
    if jt not in dic_result:
        dic_result[jt] = {}

    # 保存该维度的评分结果
    ability_score = raw_response.get(target, {})
    dic_result[jt][target] = ability_score

    # 保存元数据
    if "_scoring_metadata" not in dic_result[jt]:
        dic_result[jt]["_scoring_metadata"] = {}
    dic_result[jt]["_scoring_metadata"][target] = raw_response.get("_metadata", {})

    # 保存结果到文件
    fp_result.save(dic_result)

    # 保存详细日志
    fp_log = FileProcessor(f"log/{num}_{target}.json")
    fp_log.save(raw_response)

    # 打印简要信息
    score = ability_score.get("score", "N/A")
    level = ability_score.get("level", "N/A")
    evidence_count = ability_score.get("statistics", {}).get("evidence_count", 0)
    print(f"{jt} - {target}: {score}/100 ({level})")


