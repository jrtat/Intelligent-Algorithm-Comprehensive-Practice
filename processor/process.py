model = LLMInvoker()

def batch_extract_info(fp, source, target, prompt):
    '''
    统一批量处理单种信息提取
    :param fp: 文件处理器
    :param source: 原信息列表
    :param target: 目标信息名
    :param prompt: 提示词
    :return: 处理后的信息（内含中途保存机制）
    '''
    # 调用模型
    t = 0
    for key in tqdm(fp.data.keys(), desc="正在处理数据" + target):
        if target in fp.data[key]:
            continue
        p = f'''
        你是一个专业的信息提取分析助手。请根据提供的信息，提取【{target}】字段的内容。
        
        ### 任务：
        {prompt}
        
        ### 要求：
        1. **输出格式**：仅返回一个标准的 JSON 对象，格式为：{{"{target}": （提取的内容）}}
        2. **内容真实性**：必须严格基于原文信息，不能篡改、不能编造、不能无中生有
        3. **完整性**：确保返回的内容是完整的、有效的、与原文信息一致的、没有遗漏的
        4. **简洁性**：不要任何解释、思考过程或其他多余内容
        5. **可解析性**：确保返回的 JSON 格式正确，可以被 json.loads() 直接解析
        
        ### 提供的信息：

        '''
        # f"要求：仅返回JSON，不要额外解释，确保JSON格式可解析，key为{target}。不能篡改原文信息，不能无中生有。以下为提供信息："
        for i in source:
            p += i + ":" + fp.data[key][i] + ";"
        raw_response = model.call_ollama(p)
        if not raw_response:
            tqdm.write("岗位信息解析失败。岗位id："+key)
        else:
            fp.data[key][target] = raw_response[target]
            t += 1

            if t % 10 == 0:
                fp.save()
                tqdm.write("已保存"+str(t)+"条数据")
    fp.save()
    print("已保存所有"+str(t)+"条数据")


def batch_extract_info_together(fp, source, targets, prompt):
    '''
    统一批量处理多种信息提取（单次调用提取多个字段）
    :param fp: 文件处理器
    :param source: 原信息列表
    :param targets: 目标信息名列表
    :param prompt: 提示词
    :return: 处理后的信息（内含中途保存机制）
    '''
    t = 0
    for key in tqdm(fp.data.keys(), desc="正在批量提取多字段信息"):
        # 检查是否所有目标字段都已存在，如果都存在则跳过
        if all(target in fp.data[key] for target in targets):
            continue

        # 构建JSON格式要求字符串
        targets_format = ", ".join([f'"{target}"' for target in targets])

        p = f'''
        你是一个专业的信息提取分析助手。请根据提供的信息，一次性提取以下【{len(targets)}】个字段的内容：{', '.join(targets)}。

        ### 任务：
        {prompt}

        ### 要求：
        1. **输出格式**：仅返回一个标准的 JSON 对象，必须包含以下所有key：{{{targets_format}}}
        2. **内容真实性**：必须严格基于原文信息，不能篡改、不能编造、不能无中生有
        3. **完整性**：确保返回的每个字段都是完整的、有效的、与原文信息一致的、没有遗漏的
        4. **简洁性**：不要任何解释、思考过程或其他多余内容
        5. **可解析性**：确保返回的 JSON 格式正确，可以被 json.loads() 直接解析
        6. **缺失处理**：如果某个字段在原文中无法提取，该字段对应值设为 null 或空列表 []（根据字段类型决定）

        ### 提供的信息：
        '''

        for i in source:
            if i in fp.data[key]:
                p += i + ":" + str(fp.data[key][i]) + ";\n"

        # 调用模型
        raw_response = model.call_ollama(p)

        if not raw_response:
            tqdm.write(f"岗位信息解析失败。岗位id：{key}")
        else:
            # 将提取的各个字段保存到数据中
            success_count = 0
            for target in targets:
                if target in raw_response:
                    fp.data[key][target] = raw_response[target]
                    success_count += 1
                else:
                    tqdm.write(f"岗位信息中缺少字段：{target}。岗位id：{key}")

            if success_count > 0:
                t += 1

                # 每处理10条数据保存一次
                if t % 10 == 0:
                    fp.save()
                    tqdm.write(f"已保存{t}条数据")

    # 最终保存
    fp.save()
    print(f"已保存所有{t}条数据")



